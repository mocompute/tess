typedef struct ast_node {
    union {
        struct ast_symbol {
            string_t         name;
            string_t         original;
            struct ast_node *annotation;
            tl_type         *annotation_type;
            u64              special_hash; // for use during specialisation
            u8               flags;
        } symbol;

        struct ast_bool {
            int val;
        } bool_;

        struct ast_i64 {
            i64 val;
        } i64;

        struct ast_u64 {
            u64 val;
        } u64;

        struct ast_f64 {
            f64 val;
        } f64;

        struct ast_arrow {
            struct ast_node *left;
            struct ast_node *right;
        } arrow;

        struct ast_assignment {
            struct ast_node *name;
            struct ast_node *value;
        } assignment;

        struct ast_array {
            // all variants with arrays must use this layout
            struct ast_node **nodes;
            u8                n;
            u8                flags;
        } array;

        struct ast_begin_end {
            struct ast_node **expressions;
            u8                n_expressions;
        } begin_end;

        struct ast_lambda_function {
            struct ast_node **parameters;
            u8                n_parameters;
            u8                flags;
            struct ast_node  *body;
        } lambda_function;

        struct ast_function_declaration {
            struct ast_node **parameters;
            u8                n_parameters;
            struct ast_node  *name;
        } function_declaration;

        struct ast_lambda_declaration {
            struct ast_node **parameters;
            u8                n_parameters;
        } lambda_declaration;

        struct ast_let {
            struct ast_node **parameters;
            u8                n_parameters;
            u8                flags;
            struct ast_node  *name;
            struct ast_node  *body;
        } let;

        struct ast_lambda_application {
            struct ast_node **arguments;
            u8                n_arguments;
            struct ast_node  *lambda;
        } lambda_application;

        struct ast_named_application {
            struct ast_node **arguments;
            u8                n_arguments;
            u8                flags;
            struct ast_node  *name;
            tl_type          *function_type;
        } named_application;

        struct ast_labelled_tuple {
            struct ast_node **assignments;
            u8                n_assignments;
            u8                flags;
        } labelled_tuple;

        struct ast_tuple {
            struct ast_node **elements;
            u8                n_elements;
            u8                flags;
        } tuple;

        struct ast_user_type {
            struct ast_node **fields;
            u8                n_fields;
            struct ast_node  *name;
        } user_type; // a type literal, generated by compiler as body of constructor

        // below have no array

        struct ast_address_of {
            struct ast_node *target;
        } address_of;

        struct ast_dereference {
            struct ast_node *target;
        } dereference;

        struct ast_dereference_assign {
            struct ast_node *target;
            struct ast_node *value;
        } dereference_assign;

        struct ast_if_then_else {
            struct ast_node *condition;
            struct ast_node *yes;
            struct ast_node *no;
        } if_then_else;

        struct ast_let_in {
            struct ast_node *name;
            struct ast_node *value;
            struct ast_node *body;
        } let_in;

        struct ast_let_match_in {
            struct ast_node *lt; // labelled_tuple
            struct ast_node *value;
            struct ast_node *body;
        } let_match_in;

        struct ast_user_type_get {
            struct ast_node *struct_name;
            struct ast_node *field_name;
            u8               flags;
        } user_type_get;

        struct ast_user_type_set {
            struct ast_node *struct_name;
            struct ast_node *field_name;
            struct ast_node *value;
            u8               flags;
        } user_type_set;

        struct ast_user_type_def {
            struct ast_node  *name;
            struct ast_node **field_annotations;
            struct ast_node **field_names;
            tl_type         **field_types;
            u8                n_fields;
        } user_type_def;
    };

    char const       *file;
    u32               line;
    tl_type          *type;
    ast_tag           tag;
    enum tl_error_tag error;
} ast_node;

