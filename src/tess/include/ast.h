typedef struct ast_node {
    union {
        struct {
            string_t         name;
            string_t         original; // set by syntax_rename_variable
            struct ast_node *annotation;
        } symbol;

        struct {
            bool val;
        } bool_;

        struct {
            i64 val;
        } i64;

        struct {
            u64 val;
        } u64;

        struct {
            f64 val;
        } f64;

        struct {
            // all variants with arrays must use this layout
            struct ast_node **nodes;
            u16               n;
        } array;

        struct ast_user_type {
            struct ast_node **fields;
            u16               n_fields;
            struct ast_node  *name;
        } user_type; // a type literal, generated by compiler as body of constructor

        struct {
            ast_operator     op;
            struct ast_node *left;
            struct ast_node *right;
        } infix;

        struct {
            struct ast_node **parameters;
            u16               n_parameters;
            struct ast_node  *body;
            // node type is arrow
        } lambda_function;

        struct {
            struct ast_node *name;
            struct ast_node *value;
            struct ast_node *body;
        } let_in;

        struct {
            struct ast_node **parameters;
            u16               n_parameters;
            struct ast_node  *name;
        } function_declaration;

        struct {
            struct ast_node **parameters;
            u16               n_parameters;
        } lambda_declaration;

        struct {
            struct ast_node **parameters;
            u16               n_parameters;
            struct string     name;
            struct ast_node  *body;
            tl_type          *arrow;            // because the node type itself is nil
            struct string     specialized_name; // set during function specialisation
        } let;

        struct {
            struct ast_node *condition;
            struct ast_node *yes;
            struct ast_node *no;
        } if_then_else;

        struct {
            struct ast_node **arguments;
            u16               n_arguments;
            struct ast_node  *lambda;
        } lambda_application;

        struct {
            struct ast_node **arguments;
            u16               n_arguments;
            struct string     name;
            struct ast_node  *specialized;
        } named_application;

        struct {
            struct ast_node **elements;
            u16               n_elements;
        } tuple;

        struct ast_user_type_def {
            struct ast_node  *name;
            struct ast_node **field_annotations;
            struct ast_node **field_names;
            tl_type         **field_types;
            u16               n_fields;
        } user_type_def;
    };

    tl_type            *type;
    ast_tag             tag;
    enum tess_error_tag error;
} ast_node;

typedef struct {
    array_header;
    ast_node **v;
} ast_node_array;

typedef struct {
    array_slice;
    ast_node **v;
} ast_node_slice;

typedef struct {
    array_sized;
    ast_node **v;
} ast_node_sized;

struct ast_node_iterator {
    struct vector_iterator_base base;
    struct ast_node           **ptr;
};

